import unittest
from knights import attack, data_builder

# changing something here!
class KnightTest(unittest.TestCase):
    def test_basic(self):
        data_test = ('''\
        *************************
        *   *                   *
        *   *                   *
        * S *   *****************
        *   *   *     * *       *
        *********     * *       *
        *       *     *E*       *
        *       *     * *       *
        *************************''')  # 8 moves
        self.assertEqual(attack(*data_builder(data_test)), 8)

